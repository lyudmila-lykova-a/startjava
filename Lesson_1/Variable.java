public class Variable{
	public static void main(String[] args){
		byte ram = 4;
		short cores = 2;
		int model = 6420;
		long assembly = 18362;
		float virtualMemory = 5.94F;
		double physicalMemory = 3.88;
		char os  = 'W';
		boolean ruLangSupport = true;

		System.out.println("RAM = " + ram);
		System.out.println("Cores = " + cores);
		System.out.println("Model = " + model);
		System.out.println("Assembly = " + assembly);
		System.out.println("Virtual Memory = " + virtualMemory);
		System.out.println("Physical Memory = " + physicalMemory);
		System.out.println("OS = " + os);
		System.out.println("RU language support = " + ruLangSupport);
	}
}
